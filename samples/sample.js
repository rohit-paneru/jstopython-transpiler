// JavaScript example 1: Variable declaration and assignment
let message = "Hello";
const PI = 3.14159;
var counter = 0;

// JavaScript example 2: Conditional statement
let temperature = 25;
if (temperature > 20) {
  console.log("It's warm!");
} else {
  console.log("It's cool.");
}

// JavaScript example 3: Loop
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// JavaScript example 4: Function definition
function add(a, b) {
  return a + b;
}
let sum = add(5, 3);
console.log(sum);

// JavaScript example 5: Array manipulation
let numbers = [1, 2, 3];
numbers.push(4);
console.log(numbers);

// JavaScript example 6: Object literal
let person = {
  name: "Alice",
  age: 30,
  greet: function() {
    console.log(`Hello, my name is ${this.name}`);
  }
};
person.greet();